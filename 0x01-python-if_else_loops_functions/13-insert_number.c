#include "lists.h"

/**
 * insert_node - Add a num to a Sorted Singly Linked List
 * @head: Ptr to the Head of the List
 * @number: the value of the node
 * Return: Ptr to the node or 0 if its fails
 */
listint_t *insert_node(listint_t **head, int number)
{
	listint_t *node = *head, *new;

	new = malloc(sizeof(listint_t));
	if (new == NULL)
		return (NULL);
	new->n = number;
	if (node == NULL || node->n >= number)
	{
		new->next = node;
		*head = new;
		return (new);
	}
	while (node && node->next && node->next->n < number)
		node = node->next;
	new->next = node->next;
	node->next = new;
	return (new);
}
